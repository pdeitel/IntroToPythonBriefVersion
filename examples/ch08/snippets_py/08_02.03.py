# Section 8.2.3 snippets

# Formatting Positive Numbers with Signs
f'[{27:+10d}]'

f'[{27:+010d}]'

# Using a Space Where a + Sign Would Appear in a Positive Value
print(f'{27:d}\n{27: d}\n{-27: d}')

# Grouping Digits
f'{12345678:,d}'

f'{123456.78:,.2f}'



